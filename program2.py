a=int(input("enter value 1 : "))
b=int(input("enter value 2 : "))
option=int(input("enter your option: "))
if(option==1):
    print("solution : ",a+b)
elif(option==2):
    print("solution : ",a-b)
elif(option==3):
    print("solution : ",a*b)
elif(option==4):
    print("solution : ",a%b)
elif(option==5):
    print("solution : ",a/b)
elif(option==6):
    print("solution : ",a^b)
